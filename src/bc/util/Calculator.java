package bc.util;


public class Calculator {
	
	public static int getSum(int int1, int int2) {
		return int1+int2;
	}
	
	public static int getSum(int int1, int int2, int int3) {
		return int1+int2+int3;
	}
	
	public static double getSum(double dbl1, double dbl2) {
		return dbl1+dbl2;
	}
	
	public static double getSum(double dbl1, double dbl2, double dbl3) {
		return dbl1+dbl2+dbl3;
	}
	
}
